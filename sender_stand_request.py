# Импорт настроек из модуля configuration, который содержит параметры конфигурации, такие как URL сервиса
import configuration
# Импорт библиотеки requests для выполнения HTTP-запросов
import requests
# Импорт данных запроса из модуля data, в котором определены заголовки и тело запроса
import data
# Создаём заказ через API и возвращает его трек-номер
def create_order():
    # Формируем полный URL для создания заказа: базовый адрес + путь к эндпоинту
    url_create = configuration.URL_SERVICE + configuration.CREATE_PATH
    # Отправляем POST-запрос с данными заказа (из data.order_body) на полученный URL
    resp_create = requests.post(url_create, json=data.order_body)
    # Проверяем, что сервер вернул статус 200 или 201 (успешное создание). 
    # Если нет — тест падает с понятным сообщением об ошибке.
    assert resp_create.status_code in (200,201), f"Ошибка создания заказа: статус {resp_create}"
    # Извлекаем трек-номер из JSON-ответа сервера и приводим его к строке
    track = str(resp_create.json()["track"])
    # Выводим в консоль подтверждение и сам трек-номер для наглядности
    print(f"Заказ создан, трек: {track}")
    # Возвращаем трек-номер, чтобы использовать его в следующих шагах теста
    return track
# Получаем данные заказа по трек-номеру через API
def get_order_by_track(track):
    # Формируем URL для получения данных заказа по треку
    url_get = configuration.URL_SERVICE + configuration.GET_BY_TRACK_PATH
    # Отправляем GET-запрос, передавая трек-номер в параметрах запроса.
    resp_get = requests.get(url_get,
                            params={configuration.TRACK_PARAM_NAME:track}
                            )
    # Проверяем, что сервер вернул статус 200 (успешный ответ). Иначе тест падает.
    assert resp_get.status_code == 200, f"Ошибка получения заказа: статус {resp_get.status_code}"
    # Возвращаем JSON-ответ сервера, чтобы дальше проверить данные заказа
    return resp_get.json()
# Проверяем, что трек-номер в ответе сервера совпадает с тем, который мы передали
def validate_track_match(track, response_data):
    # Достаём данные заказа из ответа сервера
    order = response_data["order"]
    # Сравниваем трек-номер из ответа сервера с тем, который мы передали.
    # Если они не совпадают — тест падает
    assert str(order["track"]) == track, f"Трек не совпадает: {order['track']} != {track}"
    # Если всё хорошо, выводим подтверждение
    print("Треки совпадают")
# Запускает весь тестовый сценарий: создаёт заказ, получает его и сверяет трек
def run_test():
    # Создаём заказ и получаем его трек-номер
    track = create_order()
    # Получаем данные заказа
    response_data = get_order_by_track(track)
    # Проверяем, что трек в ответе сервера совпадает
    validate_track_match(track, response_data)
    # Если все проверки прошли, выводим финальное сообщение
    print("Тест пройден")
    # Возвращаем True, при успешном выполнении теста
    return True
# Запускаем тест сразу при запуске скрипта
run_test()
    




